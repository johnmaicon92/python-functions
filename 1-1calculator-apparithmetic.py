"""
Task 1: Create functions for each arithmetic operation.
"""

import math

def sum(x, y):

    return x + y

def subtract(x, y):

    return x - y

def multiply(x, y):

    return x * y


def divide(x, y):

    return x / y

print(sum(3, 5))  
print(subtract(10, 4))  
print(multiply(7, 3))  
print(divide(15, 3))  
print(divide(10, 2))  
